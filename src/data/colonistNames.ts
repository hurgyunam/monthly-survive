/**
 * 샘플 리스트에서 중복 없이 N개의 이름을 랜덤으로 선택한다.
 * @param count 필요한 이름 개수 (2~3 권장)
 * @returns 선택된 이름 배열. count가 리스트보다 크면 전체를 섞어 앞 N개 반환
 */
export function pickRandomNames(count: number): string[] {
  const shuffled = [...COLONIST_NAMES].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
}
